			<br/>
			<br/>Quelques manipulations doivent être faites avant de pouvoir profiter de votre vidéothèque. Ce ne sera pas long !
		</div>
		<div class='lead'>Définir les chemins et répertoires</div>
		<div class='gog-form-description'>
			Il faut indiquer à l'application certains emplacements nécessaires : votre répertoire contenant l'ensemble de votre vidéothèque, 
			mais aussi ceux des exécutable de VLC (pour pouvoir visionner vos films & séries) et de votre gestionnaire de fichier (pour avoir rapidement accès à vos fichiers).
			<br/>
			<br/>Pour cela rien de plus simple : cliquez sur le bouton <kbd>PARAMÈTRE</kbd> en haut de la page. Puis descendez jusqu'à atteindre <kbd>CHEMINS ET RÉPERTOIRES</kbd>. Ajoutez les chemins manquant, puis n'oubliez pas de <strong>confirmer les changements</strong> en cliquant sur le bouton correspondant.
			<br/>
			<br/>D'autres chemins sont présents, mais il n'est normalement pas nécessaire de les modifier. <strong>Ne les modifiez que si vous savez ce que vous faîtes !</strong>
			<br/>
			<br/>Vous pouvez ensuite fermer les paramètres en cliquant sur la croix, ou sur le côté.
		</div>
		<div class='lead'>Scanner les oeuvres</div>
		<div class='gog-form-description'>
			Vous avez (normalement) spécifié à l'application où aller chercher. Maintenant il faut qu'elle le fasse !
			<br/>
			<br/>Cliquez simplement sur le bouton <kbd>SCANNER</kbd> en haut de la page, puis lancez le scanner en cliquant sur le bouton correspondant.
			<br/>Tout le contenu vidéo du répertoire de votre vidéothèque sera alors affiché dans un tableau.
			<br/>
			<br/>Ajouter une des vidéos en tant qu'oeuvre dans votre vidéothèque est alors très simple.
			<br/>Cochez une des vidéos. Un bloc s'affichera alors en bas à droite de la page.
			<br/>Rentrez-y le lien Senscritique de l'oeuvre, sans erreur (un beau copier/coller <span class='glyphicon glyphicon-copy'></span>).
			<br/>
			<br/>Si l'oeuvre en question est une série, le numéro ou nom de la saison sera demandé. Entrez-le (ex: 1, 2, OAV, toto, ...).
			<br/>Toujours dans le cas d'une série, <strong>la sélection d'une vidéo s'étendra sur tout son dossier</strong>. Ainsi ce dernier représentera la saison ajoutée. Veuillez bien à organiser vos fichiers en fonction.
			<br/>
			<br/>Rentrez les langues et sous-titres de l'oeuvre en question en les séparant avec des virgules (ex: en, fr, ita).
			<br/>
			<br/>Une fois toutes les données entrées et vérifiées, cliquez sur le bouton d'ajout.
			<br/>
			<br/>Répétez cette procédure pour chaque film et série que vous souhaitez ajouter (vous n'êtes évidemment pas obligé de tout faire maintenant !).
			<br/>Quittez ensuite le scanner.
		</div>
		<div class='lead'>Charger les oeuvres</div>
		<div class='gog-form-description'>
			Vous avez ajouter une ou plusieurs oeuvres, il est maintenant tant de les charger !
			<br/>
			<br/>La partie la plus simple: cliquez simplement sur le bouton <kbd>Charger les oeuvres</kbd> juste en dessous de l'image de marmotte.
			<br/>Si vous aviez déjà cliqué sur ce bouton avant cette étape (tricheur !) et qu'il n'est plus visible, rechargez simplement la page, puis cliquez dessus.
			<br/>
			<br/>Le chargement des oeuvres dépendra de votre connexion internet et du serveur de Senscritique. Ca peut donc demander un certain temps.
			<br/>Prennez bien en compte que même si Senscritique (ou votre connexion) n'est pas accessible, l'application fonctionnera tout de même !
		</div>
		<div class='lead'>Accéder à une oeuvre</div>
		<div class='gog-form-description'>
			Lorsqu'une oeuvre est chargée, cliquez sur cette dernière pour ouvrir sa fiche.
			<br/>Vous aurez alors accès à toutes ses informations.
			<br/>
			<br/>Vous pouvez ensuite lancer le film (ou un épisode dans le cas d'une série) via le panel de visionnage, en bas.
			<br/>Pour cela cliquez simplement sur le bouton correspondant, puis <i>bon film</i> <span class='glyphicon glyphicon-thumbs-up'></span>
		</div>
