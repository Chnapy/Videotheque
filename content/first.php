			<br/>
			<br/>Si tout s'est déroulé comme prévu, essayez chaque fonctionnalité de l'application.
			<br/>
			<br/>Vous l'avez peut-être fait précédemment, connectez-vous à Senscritique ! 
			<br/>Cliquez simplement sur le bouton <kbd>SE CONNECTER</kbd> en haut à droite, puis rentrez les informations demandées et confirmez. <strong>Aucune information entrée n'est sauvegardée !</strong>
			<br/>
			<br/>Modifiez vos paramètres. Vous ne voulez plus voir ce bloc au démarrage ? Vous souhaitez charger les oeuvres automatiquement ? Indiquez-le.
